    async getData() {
      let eData = this.componentEnum.find(
        (obj) => obj.typeName == this.typeData.name
      );
      if (eData) {
        this.componentList = eData.list;
      } else {
        this.componentList = [];
      }
      try {
        const res = await componentAPI.getByCat({
          id: this.typeData.id,
          name: "",
          page_index: 0,
          page_size: 0,
        });
        if (res.code === 200) {
          this.componentList = [
            ...this.componentList.map((obj) => ({ ...obj, online: false })),
            ...res.data.list.map((obj) => ({
              ...obj,
              type: "component",
              name: obj.code,
              label: obj.name,
              online: true,
              img: "/resource_file/" + obj.preview_url,
            })),
          ];
          console.log(this.componentList);
        }
