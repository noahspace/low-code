        e.dataTransfer.setData(
          "dragData",
          JSON.stringify({
            type: "add",
            name: data.name,
          })
        );
      } else if (data.type === "config") {
        e.dataTransfer.setData(
          "dragData",
          JSON.stringify({
            type: "addConfig",
            config: data.config,
          })
        );
      }
    },
